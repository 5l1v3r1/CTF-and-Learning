#include <bits/stdc++.h>
using namespace std;

int main(){
    int n;
    cin >> n;
    if (n % 2 == 0)
    {
        cout << " hai" << endl;
    }
    else if (n % 3 == 0)
    {
        cout << "ba" << endl;
    }
    else if (n % 4 == 0)
    {
        cout << "bon" << endl;
    }

    return 0;
}
